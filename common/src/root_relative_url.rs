//! [`RootRelativeUrl`] — a validated, host-less **root-relative** URL reference (`/…`,
//! optional query), the type for the web post URLs (permalink / preview / edit) the
//! browser resolves against the current origin and that are *never* composed against a
//! base. It is a distinct grammar (ADR-0063, invariant axis) from [`AbsoluteUrl`], which
//! carries a scheme and host, and from `FeedPath`, a closed feed-endpoint identity set;
//! a `RootRelativeUrl` is any `/…` reference, so it folds onto neither.
//!
//! Validation is structural — a single leading slash (host-less, not protocol-relative
//! `//host`, not an absolute `scheme://`) and no whitespace or control characters. Call
//! sites build canonical `/…` paths, so the stored value is the trimmed input. The
//! ergonomic trailer (`Display`, `AsRef`/`Borrow`/`Deref<str>`, `TryFrom<String>`,
//! `From<Self> for String`, `PartialEq<str>`, the validating serde + sqlx bridges) is
//! generated by `#[derive(StrNewtype)]`; only the validating [`FromStr`] is hand-written.

use std::str::FromStr;

use macros::StrNewtype;
use thiserror::Error;

/// A validated, host-less root-relative URL reference (`/…`, optional query). Construct
/// via [`FromStr`]. `Hash` is derived (it may be a map key); no `Ord`.
#[derive(Clone, Debug, PartialEq, Eq, Hash, StrNewtype)]
pub struct RootRelativeUrl(String);

/// Error returned when a string is not a valid root-relative URL reference.
#[derive(Debug, Error)]
#[error("not a valid root-relative URL (must begin with '/' and be host-less)")]
pub struct InvalidRootRelativeUrl;

impl FromStr for RootRelativeUrl {
    type Err = InvalidRootRelativeUrl;

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        let s = s.trim();
        // Root-relative: a single leading slash — host-less, not protocol-relative
        // (`//host`), not an absolute `scheme://` — and no whitespace/control chars.
        if !s.starts_with('/')
            || s.starts_with("//")
            || s.contains(|c: char| c.is_whitespace() || c.is_control())
        {
            return Err(InvalidRootRelativeUrl);
        }
        Ok(Self(s.to_owned()))
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn accepts_root_relative_paths() {
        for s in [
            "/",
            "/~alice/2026/01/02/hello",
            "/draft/5/preview",
            "/a/b?x=1",
        ] {
            assert_eq!(
                s.parse::<RootRelativeUrl>().unwrap().as_ref(),
                s,
                "should accept and round-trip {s}"
            );
        }
    }

    #[test]
    fn rejects_non_root_relative() {
        for s in [
            "",
            "foo",
            "foo/bar",
            "//evil.com/x",
            "https://x/y",
            "http://h",
            "mailto:a@b",
            "/a b",      // whitespace
            "/a\u{7f}b", // control char
        ] {
            assert!(s.parse::<RootRelativeUrl>().is_err(), "should reject {s:?}");
        }
    }

    #[test]
    fn trailer_derefs_and_displays() {
        let u = "/~alice/hello".parse::<RootRelativeUrl>().unwrap();
        let s: &str = &u; // Deref<str>
        assert_eq!(s, "/~alice/hello");
        assert_eq!(u.to_string(), "/~alice/hello"); // Display
        assert_eq!(u, *"/~alice/hello"); // PartialEq<str>
    }
}
